// Responsive breakpoint utilities
export const getResponsiveClasses = (baseClasses, breakpoints = {}) => {
  const responsiveMap = {
    sm: 'sm:',
    md: 'md:',
    lg: 'lg:',
    xl: 'xl:'
  };

  let classes = baseClasses;
  Object.entries(breakpoints).forEach(([bp, bpClasses]) => {
    if (responsiveMap[bp]) {
      classes += ` ${responsiveMap[bp]}${bpClasses}`;
    }
  });

  return classes;
};

